// Identify Vessels With High STS Count For The Last 6 Months
/*
Understand which vessels are used as lightering ones or maybe participate in sanctioned cargo transfers
*/
SELECT
  LOAD_AREA,
  IMO,
  COUNT(*) AS NUMBER_OF_STS_LOADS,
  SUM(LOAD_QUANTITY_KT) AS TOTAL_STS_LOAD_KT
FROM REFINED_PRODUCTS.CARGO_TRACKING
WHERE LOAD_DATE >= DATEADD('month', -6, CURRENT_DATE())
 AND LOAD_STS_INDICATOR = 1
GROUP BY LOAD_AREA, IMO
ORDER BY NUMBER_OF_STS_LOADS DESC
LIMIT 10;
;

